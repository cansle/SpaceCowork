package kr.bar.coder.view;

public class AndroiVersionInfo {
	
	private int id;
	private double ver_num;
	private int year;
	private String Ver_name;
	private String Ver_name_kor;
	private String alphabet;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public double getVer_num() {
		return ver_num;
	}
	public void setVer_num(double ver_num) {
		this.ver_num = ver_num;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public String getVer_name() {
		return Ver_name;
	}
	public void setVer_name(String ver_name) {
		Ver_name = ver_name;
	}
	public String getVer_name_kor() {
		return Ver_name_kor;
	}
	public void setVer_name_kor(String ver_name_kor) {
		Ver_name_kor = ver_name_kor;
	}
	public String getAlphabet() {
		return alphabet;
	}
	public void setAlphabet(String alphabet) {
		this.alphabet = alphabet;
	}
	
	
}
